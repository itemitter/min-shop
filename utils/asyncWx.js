/**
 * promise 形式的 小程序的微信支付
 * @param {object} pay 支付所必要的参数
 */
export const requestPayment=(pay)=>{
  return new Promise((resolve,reject)=>{
   wx.requestPayment({
      ...pay,
     success: (result) => {
      resolve(result)
     },
     fail: (err) => {
       reject(err);
     }
   });
     
  })
}

/**
 * promise 形式的 获取用户信息
 * / 推荐使用wx.getUserProfile获取用户信息，开发者每次通过该接口获取用户个人信息均需用户确认
    // 开发者妥善保管用户快速填写的头像昵称，避免重复弹窗
 */
export const requestProfile=({desc})=>{
  return new Promise((resolve,reject)=>{
   wx.getUserProfile({
    desc,
    timeout:10000,
    success: (result) => {
      resolve(result);
    },
    fail: (err) => {
      reject(err);
    }
   });
     
  })
}

